package utils

import (
	"bytes"
	"strings"
)

func LastString(ss []string) string {
	return ss[len(ss)-1]
}

type IndexList struct {
	BeforeWord string
	StartIndex int
	EndIndex   int
}

func StrIndexAll(indexList IndexList, str, substr string) IndexList {
	// 子串在字符串的字节位置
	result := strings.Index(str, substr)
	if result >= 0 {
		// 获得子串之前的字符串并转换成[]byte
		prefix := []byte(str)[0:result]
		// 将子串之前的字符串转换成[]rune
		rs := []rune(string(prefix))
		// 获得子串之前的字符串的长度，便是子串在字符串的字符位置
		result = len(rs)

		indexList.BeforeWord = string(prefix)
		indexList.StartIndex = result
		indexList.EndIndex = result + len([]rune(substr)) - 1
	}
	return indexList
}

func SplitSubN(s string, n int) []string {
	sub := ""
	subs := []string{}

	runes := bytes.Runes([]byte(s))
	l := len(runes)
	for i, r := range runes {
		sub = sub + string(r)
		if (i+1)%n == 0 {
			subs = append(subs, sub)
			sub = ""
		} else if (i + 1) == l {
			subs = append(subs, sub)
		}
	}

	return subs
}

func IsContain(items []string, item string) bool {
	for _, eachItem := range items {
		if eachItem == item {
			return true
		}
	}
	return false
}
